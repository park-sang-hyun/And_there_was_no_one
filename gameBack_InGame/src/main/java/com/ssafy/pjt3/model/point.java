package com.ssafy.pjt3.model;

public class point {
	String who;
	int cnt;
	public point(String who, int cnt) {
		this.who = who;
		this.cnt = cnt;
	}
	public String getWho() {
		return who;
	}
	public void setWho(String who) {
		this.who = who;
	}
	public int getCnt() {
		return cnt;
	}
	public void setCnt(int cnt) {
		this.cnt = cnt;
	}
	
	
}
